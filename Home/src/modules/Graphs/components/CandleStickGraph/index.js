import React, { Component } from 'react';
import PropTypes from 'prop-types';
import {connect} from "react-redux";
import {onLoadTrigger} from "../../../helpers";
import CandleStickGraphCanvas from './CandleStickGraphCanvas.js';
import '../style.css';
import _ from "lodash";

class CandleStickGraph extends Component { // eslint-disable-line react/prefer-stateless-function

    constructor(props) {
        super(props);
        this.state = {
            value: '',
            redraw: false,
            graph: null
        };
    }

    componentWillMount() {
        const { actions, dispatch } = this.props;
        onLoadTrigger(actions, dispatch)
        this.setState({graph: this.returnGraph()})
        window.addEventListener('resize', this.handleResize)
    }


    componentWillUnmount() {
        window.removeEventListener('resize', this.handleResize)
    }

    shouldComponentUpdate(nextProps, nextState, nextContext) {
        const {redraw: nextRedraw} = nextState
        const {redraw } = this.state
        if (!redraw){
            this.setState({redraw: true, graph: null})
        }

        return (
          nextProps !== this.props ||
          nextState !== this.state
        );
    }

    componentDidUpdate(prevProps, prevState, snapshot) {
        const {redraw: prevRedraw} = prevState
        const {redraw} = this.state
        if (!prevRedraw && redraw){
            this.setState({redraw: false, graph:this.returnGraph()})
        }
    }


    handleResize = ()=>{
        this.setState({redraw: true, graph: null})
        this.debounceRedraw()
    }

    handleRedraw = () =>{
        setTimeout(()=>{
            this.setState({redraw: false, graph: this.returnGraph()})
        })
    }
    debounceRedraw = _.debounce(this.handleRedraw, 100)


    returnGraph = () =>{
        const { style, store, dataSet, defaultData, defaultData3 } = this.props;
        let data = defaultData3
        if (dataSet) {
            if (typeof dataSet === "string") {
                data = store[dataSet] ? store[dataSet] : defaultData3
            } else {
                data = dataSet
            }
        }
        return <CandleStickGraphCanvas
          data={data}
          {...this.props}
        />
    }

    render() {
        const { style} = this.props;

        return (
            <div className="dittofi-graph-container" ref={this.graphCanvas} {...this.props.inline} style={style}>
                {this.state.graph}
            </div>
        );
    }
}


